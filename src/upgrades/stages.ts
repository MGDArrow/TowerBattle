export const STAGE_POINT = {
  lvl: 1, // Данный уровень
  get stage() {
    return Math.ceil(this.lvl / 3);
  }, // Данный этап
  get access() {
    return (this.stage - 1) * 300;
  }, // Общая сумма lvl необходимая для открытия этого этапа
  waves: 10, // Лучший результат
};

function getStageAwardWave(
  leftCount: number,
  leftType: TStageAwardType,
  rightCount: number,
  rightType: TStageAwardType,
): IStageAwardWave {
  return {
    left: {
      count: leftCount,
      type: leftType,
      received: false,
    },
    right: {
      count: rightCount,
      type: rightType,
      received: false,
    },
  };
}

